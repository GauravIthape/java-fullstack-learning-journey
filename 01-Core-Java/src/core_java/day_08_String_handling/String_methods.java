package day_08_String_handling;

public class String_methods {

	public static void main(String[] args) {
		String name="gaurav";
		String name1="saurav";
		System.out.println("this is length method =>"+name.length());
		System.out.println("this is touppercasem method =>"+name.toUpperCase());
		System.out.println("this is charAt method =>"+name.charAt(1));
		System.out.println("this is concat method => "+name.concat("programmer"));
		System.out.println("this is indexod methos =>"+name.indexOf("g"));
		System.out.println("this is equal method =>"+name.equals(name1));
		
	}

}
